    <h1>F1 League Racing</h1>
    <p>F1 League Racing is een online competitie gebaseerd op het populaire videospel F1 24. Hierin nemen spelers deel aan virtuele Formule 1-races, waarbij ze het opnemen tegen andere spelers van over de hele wereld. De game biedt een realistische race-ervaring, waarbij strategie en vaardigheid een cruciale rol spelen om als eerste over de finish te komen. Of je nu solo wilt racen of in teams, F1 League Racing biedt een intense en uitdagende race-ervaring.</p>

    <h2>F1 Sim Racing</h2>
    <p>F1 Sim Racing is de officiële eSports-competitie van de Formule 1. Hier nemen de beste virtuele coureurs ter wereld het tegen elkaar op in een serie races die de echte F1-kalender volgt. Deze competitie staat bekend om zijn hoge niveau en professionele aanpak, waarbij elke milliseconde telt.</p>
    <img src="./images/F1eSports.jpg" alt="Huidige F1 grid" width="500" height="300">

    <p>In de F1 eSports Series vertegenwoordigen coureurs echte Formule 1-teams en strijden ze om het kampioenschap. Twee opvallende Nederlandse coureurs, Jarno Opmeer en Thomas Ronhaar, behoren tot de beste in deze competitie. Jarno Opmeer, voormalig wereldkampioen, staat bekend om zijn nauwkeurige rijstijl en slimme strategieën. Thomas Ronhaar, een rijzende ster, heeft al laten zien dat hij een serieuze concurrent is voor de titel.</p>

    <p>F1 Sim Racing biedt een spannende kijkervaring voor fans van zowel echte als virtuele autosport, en laat zien dat eSports en traditionele sport steeds dichter naar elkaar groeien.</p>
