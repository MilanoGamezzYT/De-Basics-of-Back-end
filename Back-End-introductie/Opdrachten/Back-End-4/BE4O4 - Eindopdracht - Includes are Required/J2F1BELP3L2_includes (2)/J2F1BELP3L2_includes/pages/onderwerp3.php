    <h1>Voetbal</h1>
    <p>Voetbal is een van de meest populaire sporten ter wereld. Het wordt gespeeld tussen twee teams van elk elf spelers. Het doel van het spel is om meer doelpunten te scoren dan de tegenstander. Het spel wordt gespeeld op een rechthoekig veld met een doel aan elk uiteinde. De bal mag worden gespeeld met de voeten, het hoofd en het lichaam, maar niet met de handen, behalve door de doelman binnen zijn eigen strafschopgebied.</p>
    <p>De wedstrijd bestaat uit twee helften van elk 45 minuten. Het spel begint met een aftrap en kan worden onderbroken voor blessures of andere omstandigheden. De scheidsrechter zorgt ervoor dat de regels worden nageleefd en kan gele of rode kaarten geven voor overtredingen. Bij een gelijkspel kan het spel in sommige competities verlengd worden met extra tijd of strafschoppen.</p>
    <img src="./images/Voetbal.jpg" alt="Live voetbal wedstrijd" width="500" height="300">
    <p>Er zijn veel belangrijke toernooien in het voetbal, zoals het WK en het EK. Deze toernooien brengen teams van over de hele wereld samen en zijn vaak het hoogtepunt van het internationale voetbalseizoen. Nationale competities, zoals de Eredivisie in Nederland en de Premier League in Engeland, zijn ook van groot belang en trekken veel aandacht van fans.</p>

    <p>Populaire voetbalclubs zoals FC Barcelona, Real Madrid, en Manchester United hebben wereldwijd miljoenen fans. Deze clubs hebben vaak een rijke geschiedenis en hebben vele prijzen gewonnen. De wedstrijden van deze clubs worden vaak wereldwijd uitgezonden en gevolgd door een groot publiek.</p>
